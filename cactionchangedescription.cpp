CActionChangeDescription::CActionChangeDescription(const std::string &description,std::shared_ptr<IAction> iAction_Ptr)
{ 
 Description=description;
 iAction_NextPtr=iAction_Ptr;
 Init();
}
//----------------------------------------------------------------------------------------------------
//деструктор
//----------------------------------------------------------------------------------------------------
CActionChangeDescription::~CActionChangeDescription()
{
}

