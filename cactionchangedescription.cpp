//----------------------------------------------------------------------------------------------------
//выполнить действие с элементом
//----------------------------------------------------------------------------------------------------
void CActionChangeDescription::Execute(std::shared_ptr<IPart> iPart_Ptr,CGameState &cGameState)
{
 iPart_Ptr->Description=Description;
 if (iAction_NextPtr.get()!=NULL) iAction_NextPtr->Execute(iPart_Ptr,cGameState);	
}
//----------------------------------------------------------------------------------------------------
//инициализация
//----------------------------------------------------------------------------------------------------
void CActionChangeDescription::Init(void)
{
 if (iAction_NextPtr.get()!=NULL) iAction_NextPtr->Init();
}