CActionSetEnabled::CActionSetEnabled(bool enabled,std::shared_ptr<IAction> iAction_Ptr)
{
 Enabled=enabled;
 iAction_NextPtr=iAction_Ptr;
 Init();
}
//----------------------------------------------------------------------------------------------------
//деструктор
//----------------------------------------------------------------------------------------------------
CActionSetEnabled::~CActionSetEnabled()
{
}

