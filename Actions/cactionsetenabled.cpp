//----------------------------------------------------------------------------------------------------
//выполнить действие с элементом
//----------------------------------------------------------------------------------------------------
void CActionSetEnabled::Execute(std::shared_ptr<IPart> iPart_Ptr,CGameState &cGameState)
{
 if (Enabled==true) iPart_Ptr->Enable();
               else iPart_Ptr->Disable();
 if (iAction_NextPtr.get()!=NULL) iAction_NextPtr->Execute(iPart_Ptr,cGameState);	
}
//----------------------------------------------------------------------------------------------------
//инициализация
//----------------------------------------------------------------------------------------------------
void CActionSetEnabled::Init(void)
{
 if (iAction_NextPtr.get()!=NULL) iAction_NextPtr->Init();
}