CActionEnergyUpdate::CActionEnergyUpdate(int32_t d_energy,std::shared_ptr<IAction> iAction_Ptr)
{
 dEnergy=d_energy;
 iAction_NextPtr=iAction_Ptr;
 Init();
}
//----------------------------------------------------------------------------------------------------
//деструктор
//----------------------------------------------------------------------------------------------------
CActionEnergyUpdate::~CActionEnergyUpdate()
{
}

