CActionSingle::CActionSingle(std::shared_ptr<IAction> iAction_Ptr)
{
 iAction_NextPtr=iAction_Ptr;
 Init();
}
//----------------------------------------------------------------------------------------------------
//деструктор
//----------------------------------------------------------------------------------------------------
CActionSingle::~CActionSingle()
{
}

