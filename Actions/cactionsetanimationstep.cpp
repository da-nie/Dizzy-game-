CActionSetAnimationStep::CActionSetAnimationStep(int32_t frame,std::shared_ptr<IAction> iAction_Ptr)
{ 
 Frame=frame;
 iAction_NextPtr=iAction_Ptr;
 Init();
}
//----------------------------------------------------------------------------------------------------
//деструктор
//----------------------------------------------------------------------------------------------------
CActionSetAnimationStep::~CActionSetAnimationStep()
{
}

