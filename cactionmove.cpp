//----------------------------------------------------------------------------------------------------
//конструктор
//----------------------------------------------------------------------------------------------------
CActionMove::CActionMove(int32_t dx,int32_t dy,std::shared_ptr<IAction> iAction_Ptr)
{
 dX=dx;
 dY=dy;
 iAction_NextPtr=iAction_Ptr;
 Init();
}
//----------------------------------------------------------------------------------------------------
//деструктор
//----------------------------------------------------------------------------------------------------
CActionMove::~CActionMove()
{
}

