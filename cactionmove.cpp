//----------------------------------------------------------------------------------------------------
//выполнить действие с элементом
//----------------------------------------------------------------------------------------------------
void CActionMove::Execute(std::shared_ptr<IPart> iPart_Ptr,CGameState &cGameState)
{
 iPart_Ptr->BlockPosX+=dX;
 iPart_Ptr->BlockPosY+=dY;
 if (iAction_NextPtr.get()!=NULL) iAction_NextPtr->Execute(iPart_Ptr,cGameState);	
}
//----------------------------------------------------------------------------------------------------
//инициализация
//----------------------------------------------------------------------------------------------------
void CActionMove::Init(void)
{
 if (iAction_NextPtr.get()!=NULL) iAction_NextPtr->Init();
}