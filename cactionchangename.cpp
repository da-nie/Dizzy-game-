void CActionChangeName::Execute(std::shared_ptr<IPart> iPart_Ptr,CGameState &cGameState)
{
 iPart_Ptr->Name=Name;
 if (iAction_NextPtr.get()!=NULL) iAction_NextPtr->Execute(iPart_Ptr,cGameState);	
}