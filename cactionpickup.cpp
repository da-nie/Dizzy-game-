void CActionPickUp::Execute(std::shared_ptr<IPart> iPart_Ptr,CGameState &cGameState)
{
 cGameState.AddTake(iPart_Ptr);
 if (iAction_NextPtr.get()!=NULL) iAction_NextPtr->Execute(iPart_Ptr,cGameState);	
}