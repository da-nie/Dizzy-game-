//----------------------------------------------------------------------------------------------------
//конструктор
//----------------------------------------------------------------------------------------------------
CActionAddScore::CActionAddScore(int32_t d_score,std::shared_ptr<IAction> iAction_Ptr)
{
 dScore=d_score;
 iAction_NextPtr=iAction_Ptr;
 Init();
}
//----------------------------------------------------------------------------------------------------
//деструктор
//----------------------------------------------------------------------------------------------------
CActionAddScore::~CActionAddScore()
{
}

//****************************************************************************************************
//закрытые функции
