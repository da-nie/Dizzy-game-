//----------------------------------------------------------------------------------------------------
//выполнить действие с элементом
//----------------------------------------------------------------------------------------------------
void CActionChangeNameGlobal::Execute(std::shared_ptr<IPart> iPart_Ptr,CGameState &cGameState)
{
 std::string last_name=iPart_Ptr->Name;
 iPart_Ptr->Name=Name;
 size_t size=cGameState.MapNamed.size();
 for(size_t n=0;n<size;n++)
 {
  if (cGameState.MapNamed[n]->Name.compare(last_name)==0) cGameState.MapNamed[n]->Name=Name;
 }
 if (iAction_NextPtr.get()!=NULL) iAction_NextPtr->Execute(iPart_Ptr,cGameState);	
}
//----------------------------------------------------------------------------------------------------
//инициализация
//----------------------------------------------------------------------------------------------------
void CActionChangeNameGlobal::Init(void)
{
 if (iAction_NextPtr.get()!=NULL) iAction_NextPtr->Init();
}