CActionChangeNameGlobal::CActionChangeNameGlobal(const std::string &name,std::shared_ptr<IAction> iAction_Ptr)
{ 
 Name=name;
 iAction_NextPtr=iAction_Ptr;
 Init();
}
//----------------------------------------------------------------------------------------------------
//деструктор
//----------------------------------------------------------------------------------------------------
CActionChangeNameGlobal::~CActionChangeNameGlobal()
{
}

